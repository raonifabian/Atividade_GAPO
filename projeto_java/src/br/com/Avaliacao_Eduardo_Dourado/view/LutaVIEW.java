    private void alterar() {
    try {
        lutaDTO.setNome_combate(nome_combate.getText());
        lutaDTO.setCod_combate(cod_combate.getText());
        lutaDTO.setCusto_aposta_combate(Double.parseDouble(custo_aposta_combate.getText()));

        int[] selectedRows = jtl_consultar_lutador.getSelectedRows();

        if (selectedRows.length != 2) {
            JOptionPane.showMessageDialog(null, "Selecione exatamente dois lutadores.");
            return;
        }

        int id1 = Integer.parseInt(String.valueOf(jtl_consultar_lutador.getValueAt(selectedRows[0], 0)));
        int id2 = Integer.parseInt(String.valueOf(jtl_consultar_lutador.getValueAt(selectedRows[1], 0)));

        if (id1 == id2) {
            JOptionPane.showMessageDialog(null, "Os dois lutadores devem ser diferentes.");
            return;
        }

        LutadorDTO lutador1 = new LutadorDTO();
        LutadorDTO lutador2 = new LutadorDTO();
        lutador1.setId_lut(id1);
        lutador2.setId_lut(id2);

        JOptionPane.showMessageDialog(null,
                lutaCTR.alterarLuta(lutaDTO, lutador1, lutador2));

    } catch (Exception e) {
        System.out.println("Erro ao Alterar: " + e.getMessage());
    }
}

    
    private void excluir(){
    if(JOptionPane.showConfirmDialog(null, "Deseja Realmente excluir a Luta?", "Aviso", 
            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null,
                    lutaCTR.excluirLuta(lutaDTO));
    }
}
    
