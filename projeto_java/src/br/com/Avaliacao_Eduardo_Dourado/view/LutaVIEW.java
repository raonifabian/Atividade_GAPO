    private void preencheCamposCombate(int id_combate) {
    try {
        lutaDTO.setId_combate(id_combate);
        rs = lutaCTR.consultarLuta(lutaDTO, 2);
        if (rs.next()) {
            limpaCampos();
            nome_combate.setText(rs.getString("nome_combate"));
            cod_combate.setText(rs.getString("cod_combate"));
            custo_aposta_combate.setText(rs.getString("custo_aposta_combate"));
            
            
            model_jtl_consultar_lutador.setNumRows(0);
            model_jtl_consultar_lutador.addRow(new Object[]{
                rs.getInt("lutador1_id"),
                rs.getString("lutador1_nome")
            });
            model_jtl_consultar_lutador.addRow(new Object[]{
                rs.getInt("lutador2_id"),
                rs.getString("lutador2_nome")
            });

            jtl_consultar_lutador.setRowSelectionInterval(0, 0);
            
            gravar_alterar = 2;
            liberaCampos(true);
        }
    } catch (Exception e) {
        System.out.println("Erro ao preencheCamposProduto" + e.getMessage());
    } finally {
        lutaCTR.CloseDB();
    }
}

