package br.com.Avaliacao_Eduardo_Dourado.dto;

public class ApostadorDTO {
    private String nome_apo, logradouro_apo, bairro_apo, cidade_apo;
    private String estado_apo, cep_apo, cpf_apo;
    private int id_apo, numero_apo;

    public int getId_apo() {
        return id_apo;
    }

    public void setId_apo(int id_apo) {
        this.id_apo = id_apo;
    }

    public int getNumero_apo() {
        return numero_apo;
    }

    public void setNumero_apo(int numero_apo) {
        this.numero_apo = numero_apo;
    }

    public String getNome_apo() {
        return nome_apo;
    }

    public void setNome_apo(String nome_apo) {
        this.nome_apo = nome_apo;
    }

    public String getLogradouro_apo() {
        return logradouro_apo;
    }

    public void setLogradouro_apo(String logradouro_apo) {
        this.logradouro_apo = logradouro_apo;
    }

    public String getBairro_apo() {
        return bairro_apo;
    }

    public void setBairro_apo(String bairro_apo) {
        this.bairro_apo = bairro_apo;
    }

    public String getCidade_apo() {
        return cidade_apo;
    }

    public void setCidade_apo(String cidade_apo) {
        this.cidade_apo = cidade_apo;
    }

    public String getEstado_apo() {
        return estado_apo;
    }

    public void setEstado_apo(String estado_apo) {
        this.estado_apo = estado_apo;
    }

    public String getCep_apo() {
        return cep_apo;
    }

    public void setCep_apo(String cep_apo) {
        this.cep_apo = cep_apo;
    }

    public String getCpf_apo() {
        return cpf_apo;
    }

    public void setCpf_apo(String cpf_apo) {
        this.cpf_apo = cpf_apo;
    }

    
}
